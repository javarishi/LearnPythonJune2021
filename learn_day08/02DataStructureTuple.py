# Tuples are written with round brackets.
sampleTuple = ("Smyrna", "Macon", "Marietta", "Duluth")
# Access Element: sampleTuple[1]
print(sampleTuple)
print(sampleTuple[1])

for eachItem in sampleTuple:
    print(eachItem)

# You cannot add, remove, delete element from Tuple
del sampleTuple
print(sampleTuple)