# Mathematical
first_var = 100
second_var = 10

print("Addition + " , (first_var + second_var))
print("Subtraction - " , (first_var - second_var))
print("Multiplication * " , (first_var * second_var))
print("Division / " , (first_var / second_var))
print("Modulus % " , (first_var % second_var))
print("Rest to Power ** " , (first_var ** second_var))

# Comparison Operator - always generates boolean results
# of type bool
result = first_var > second_var
print(result, type(result))

print("Greater than > " , first_var > second_var)
print("Less than < " , first_var < second_var)
print("Greater than or equals >= " , first_var >= second_var)
print("Less than or equal <= " , first_var <= second_var)
print("Equals == " , first_var == second_var)
print("Not Equals != " , first_var != second_var)